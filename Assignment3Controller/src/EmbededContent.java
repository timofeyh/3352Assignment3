
public class EmbededContent 
{
	private String embededURL;
	private String EmbededFile;
	public String getEmbededURL() {
		return embededURL;
	}
	public void setEmbededURL(String embededURL) {
		this.embededURL = embededURL;
	}
	public String getEmbededFile() {
		return EmbededFile;
	}
	public void setEmbededFile(String embededFile) {
		EmbededFile = embededFile;
	}

}
